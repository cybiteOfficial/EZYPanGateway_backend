  async add(req, res) {
    try {
      const { moduleGroup } = req.body;

      const dataExist = await this.AllAccessFieldsModel.find({
        moduleGroup: moduleGroup.toUpperCase(),
      });
      if (dataExist.length) {
        throw new HttpException(
          'Another module already exist with the same details.',
          HttpStatus.OK,
        );
      }

      req.body.moduleGroup = req.body.moduleGroup.toUpperCase();
      const result = await new this.AllAccessFieldsModel({
        ...req.body,
      }).save();

      if (!result) {
        throw new HttpException('Unable to add data.', HttpStatus.OK);
      }

      this.addLogFunction.logAdd(
        req,
        req.userData?.type || '',
        req.userData?.Id || '',
        'ACCESS_FIELD',
        'ACCESS_FIELD_ADD',
        result._id,
        true,
        200,
        `${req.userData?.type || ''} ${
          req.userData?.contactNumber || ''
        } added access fields at ${moment()
          .utcOffset('+05:30')
          .format('YYYY-MM-DD HH:mm:ss')}.`,
        'Data added successfully.',
        req.socket.remoteAddress,
      );

      return res.status(200).send({
        message: 'Data added successfully.',
        status: true,
        data: result,
      });
    } catch (err) {
      const errData = errorRes(err);
      this.addLogFunction.logAdd(
        req,
        req.userData?.type || '',
        req.userData?.Id || '',
        'ACCESS_FIELD',
        'ACCESS_FIELD_ADD',
        '',
        errData.resData.status,
        errData.statusCode,
        `${req.userData?.type || ''} ${
          req.userData?.contactNumber || ''
        } tried to add access fields with this credentials at ${moment()
          .utcOffset('+05:30')
          .format('YYYY-MM-DD HH:mm:ss')}.`,
        errData.resData.message,
        req.socket.remoteAddress,
      );

      return res.status(errData.statusCode).send({ ...errData.resData });
    }
  }

